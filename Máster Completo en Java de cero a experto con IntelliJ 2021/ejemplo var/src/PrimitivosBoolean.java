public class PrimitivosBoolean {

    public static void main(String[] args) {

        boolean bool = true;
        System.out.println("bool = " + bool);

        double d = 2.154e-3;
        System.out.println("d = " + d);
        float f = 1.4548e2f;
        System.out.println("f = " + f);

        bool = d > f;
        System.out.println("bool = " + bool);

        boolean igual = 3-2 == 1;
        System.out.println("igual = " + igual);

    }

}
